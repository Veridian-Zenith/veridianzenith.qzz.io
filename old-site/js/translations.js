    changeLanguage(languageCode) {
        if (!this.availableLanguages.includes(languageCode)) {
            console.warn(`Language ${languageCode} not supported`);
            return;
        }

        this.currentLanguage = languageCode;
        localStorage.setItem('preferredLanguage', languageCode);
        this.applyLanguage(languageCode);

        // Trigger custom event for other components
        window.dispatchEvent(new CustomEvent('languageChanged', {
            detail: { language: languageCode }
        }));
    }

    applyLanguage(languageCode) {
        const elements = document.querySelectorAll('[data-translate]');
        const translationData = this.translations[languageCode] || this.translations['en'];

        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = translationData[key];

            if (translation) {
                // Handle different element types
                if (element.tagName === 'INPUT' && element.type === 'placeholder') {
                    element.placeholder = translation;
                } else if (element.tagName === 'META') {
                    element.content = translation;
                } else {
                    element.textContent = translation;
                }
            }
        });

        // Update document language and direction
        document.documentElement.lang = languageCode;

        // Update meta description for SEO
        const metaDescription = document.querySelector('meta[name="description"]');
        if (metaDescription) {
            metaDescription.setAttribute('content', translationData['hero.description'] || '');
        }

        // Add loading animation
        this.animateLanguageChange();
    }

    animateLanguageChange() {
        const animatedElements = document.querySelectorAll('h1, h2, h3, p, [data-translate]');

        anime({
            targets: animatedElements,
            opacity: [1, 0.5, 1],
            duration: 300,
            easing: 'easeInOutQuad'
        });
    }

    setupAutoDetection() {
        // Auto-detect browser language if no preference set
        if (!localStorage.getItem('preferredLanguage')) {
            const browserLang = navigator.language.split('-')[0];
            if (this.availableLanguages.includes(browserLang)) {
                this.changeLanguage(browserLang);
            }
        }
    }

    getCurrentLanguage() {
        return this.currentLanguage;
    }

    getAvailableLanguages() {
        return this.availableLanguages;
    }

    getTranslation(key, languageCode = this.currentLanguage) {
        return this.translations[languageCode]?.[key] || this.translations['en']?.[key] || key;
    }

